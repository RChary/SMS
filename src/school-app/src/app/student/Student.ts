export class Student {
    id: number;
    rollNo : number;
    firstName: string;
    age: number;
    fName : string;
 

  constructor(id:number,rollNo: number, firstName: string, age: number,fName : string){
      this.id=id;
    this.rollNo = rollNo;
    this.firstName = firstName;
    this.age = age;
    this.fName=fName; 
   
  }

}